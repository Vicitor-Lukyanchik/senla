package com.senla.hotel.modul.menuitem;

import com.senla.hotel.domain.Hotel;
import com.senla.hotel.modul.Menu;
import com.senla.hotel.modul.action.Action;
import com.senla.hotel.modul.action.AddService;

public class AddServiceItem implements MenuItem {

    private static final String TITLE = "Add service";

    private Action action;
    private Menu nextMenu;

    public AddServiceItem(Hotel hotel, Menu nextMenu) {
        action = new AddService(hotel);
        this.nextMenu = nextMenu;
    }

    @Override
    public void doAction() {
        action.execute();
    }

    @Override
    public Menu getNextMenu() {
        return nextMenu;
    }

    @Override
    public String getTitle() {
        return TITLE;
    }
}
